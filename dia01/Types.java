package dia01;

public class Types {
    public static void main(String[] args) {
        // Java é fortemente tipado, toda variável deve ter um tipo declarado.
        // Aqui estão alguns tipos primitivos de variáveis:
        int idade = 19; // Inteiro
        double altura = 1.82; // Ponto flutuante
        boolean temCNH = true; // Booleano
        char primeiraLetra = 'G'; // Caractere

        // Vale notar que String não é um tipo primitivo.
        String nomeCompleto = "Geovane Saraiva da Silva";
    }
}